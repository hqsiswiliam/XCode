#include <stdio.h>

int main()
{
    for(int x=0;x<=500;x+=5){
        printf("%i\n",x);
    }
    
    for(int i =1000; i>-1;i-=13){
        printf("The value of i is %i\n",i);
    }
    return 0;
}

